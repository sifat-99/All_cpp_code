#include<bits/stdc++.h>
using namespace std;
#define PRINT_VAR(x) cout << #x << " "

int main()
{
  int a, b, c;
  cout << "Enter three numbers for a,b,c: ";
  cin >> a >> b >> c;

cout<<"variables: ";
  PRINT_VAR(a);
  PRINT_VAR(b);
  PRINT_VAR(c);
  cout<<endl;

  cout<<"values: "<<a<<" "<<b<<" "<<c;
  
  cout << endl;

  return 0;
}